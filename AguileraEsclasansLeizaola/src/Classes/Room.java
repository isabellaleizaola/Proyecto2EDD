package Classes;

/**
 *
 * @author Isabella
 */
public class Room {
    //num_hab,tipo_hab,piso
    private int number;
    private String type;
    private int floor;

    public Room(int number, String type, int floor) {
        this.number = number;
        this.type = type;
        this.floor = floor;
    }

    public Room() {
    }
    
    
    
    
    /**
     * @return the number
     */
    public int getNumber() {
        return number;
    }

    /**
     * @param number the number to set
     */
    public void setNumber(int number) {
        this.number = number;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the floor
     */
    public int getFloor() {
        return floor;
    }

    /**
     * @param floor the floor to set
     */
    public void setFloor(int floor) {
        this.floor = floor;
    }
    
    
}
